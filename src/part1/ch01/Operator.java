package part1.ch01;

public class Operator {
    public static void main(String[] args) {
        int intValue = 10;
        intValue += 5;
        System.out.println(intValue);
        intValue -= 5;
        System.out.println(intValue);
        intValue *= 5;
        System.out.println(intValue);
        intValue /= 5;
        System.out.println(intValue);
        intValue %= 5;
        System.out.println(intValue);
    }
}
